#include "libft.h"
#include <string.h>

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	int i;
	int b;

	i = 0;
	b = 0;
	while (s1[i] != '\0')
		i++;
	while (b < (int)n && s2[b] != '\0')
	{
		s1[i] = s2[b];
		i++;
		b++;
	}
	s1[i] = '\0';
	return (s1);
}
