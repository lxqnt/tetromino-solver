#include "libft.h"

void	ft_strclr(char *s)
{
	int i;
	int b;

	b = 0;
	if (s != '\0')
	{
		i = ft_strlen(s);
		while (b <= i)
		{
			s[b] = '\0';
			b++;
		}
	}
}
