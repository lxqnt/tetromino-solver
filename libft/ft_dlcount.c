#include "libft.h"

int		ft_dlcount(const char *s, char d)
{
	int		len;
	int		i;

	len = 0;
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == d)
			len++;
		i++;
	}
	return (len);
}
