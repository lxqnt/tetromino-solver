#include "libft.h"

int	ft_wordcount(char const *s, char d)
{
	int c;
	int i;

	i = 0;
	c = 0;
	if (s[0] != d && s[0] != '\0')
	{
		c++;
		i++;
	}
	while (s[i] != '\0')
	{
		if (s[i] != d && s[i - 1] == d)
			c++;
		i++;
	}
	return (c);
}
